    private void JBT_InsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBT_InsertActionPerformed
        // TODO add your handling code here:
        String ID = JTF_ID.getText();
        String Name = JTF_Name.getText();
        String ClassID = JCB_ClassID.getSelectedItem().toString();
        boolean Gender = true;
        if (JRB_Male.isSelected()) {
            Gender = true;
        }
        if (JRB_Female.isSelected()) {
            Gender = false;
        }

        double Mark = Double.parseDouble(JTF_Mark.getText());

        if (ps.insertStudent(ID, Name, ClassID, Gender, Mark)) {
            JLB_Result.setText("Thêm thành công");
            RefreshTable();
        } else
            JLB_Result.setText("Thêm không thành công");
    }//GEN-LAST:event_JBT_InsertActionPerformed

