    private void RefreshTable() {
        ArrayList<Student> lst = ps.getListStudent();
        for (int i = 0; i < lst.size(); i++) {
            Student s = (Student) lst.get(i);
            Vector tbRow = new Vector();
            tbRow.add(s.getID());
            tbRow.add(s.getName());
            tbRow.add(s.getClassID());
            if (s.isGender()) {
                tbRow.add("Nam");
            } else {
                tbRow.add("Nữ");
            }
            tbRow.add(s.getMark());
            tbRow.add(s.rank());
            tbContent.add(tbRow);
        }
        dtm.setDataVector(tbContent, tbHeader);
        JTable_Student.setModel(dtm);
    }

